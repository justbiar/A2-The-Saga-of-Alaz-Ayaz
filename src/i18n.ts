export type Lang = keyof typeof translations;
export type TransKey = keyof typeof translations['tr'];

let currentLang: Lang = 'tr';

export function setLang(lang: Lang): void {
    currentLang = lang;
    localStorage.setItem('a2lang', lang);
}

export function getLang(): Lang {
    return currentLang;
}

export function t(key: TransKey): string {
    return translations[currentLang][key] ?? translations['tr'][key] ?? key;
}

// Restore saved language
const saved = localStorage.getItem('a2lang') as Lang | null;
if (saved && translations[saved]) currentLang = saved;
